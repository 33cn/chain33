package runtime

// EVM操作码定义，本质上就是一个字节，所以操作码最多只支持256个
type OpCode byte

// 是否为压栈操作
func (op OpCode) IsPush() bool {
	if op >= PUSH1 && op <= PUSH32 {
		return true
	}
	return false
}

// 是否为跳转操作
func (op OpCode) IsStaticJump() bool {
	return op == JUMP
}

